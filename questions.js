/**
 * دالة لجلب سؤال عشوائي يبدأ بحرف معين
 * @param {string} letter - الحرف المختار (مثلاً: 'أ')
 */
function getQuestionByLetter(letter) {
    const questions = questionsBank[letter];
    
    if (questions && questions.length > 0) {
        const randomIndex = Math.floor(Math.random() * questions.length);
        return questions[randomIndex];
    } else {
        return { q: `عذراً، لا يوجد أسئلة حالياً تبدأ بحرف ${letter}`, a: "" };
    }
}

/**
 * دالة لجلب مجموعة عشوائية من الأسئلة من كافة الحروف
 */
function getRandomQuestions(count = 20) {
    let allQuestions = [];
    
    // تحويل الكائن إلى مصفوفة شاملة
    Object.values(questionsBank).forEach(letterGroup => {
        allQuestions = [...allQuestions, ...letterGroup];
    });

    // خلط الأسئلة (Fisher-Yates Shuffle)
    for (let i = allQuestions.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [allQuestions[i], allQuestions[j]] = [allQuestions[j], allQuestions[i]];
    }

    return allQuestions.slice(0, count);
}

