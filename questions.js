// دالة لجلب أسئلة عشوائية من كل التصنيفات
function getRandomQuestions(count = 50) {
    let allQuestions = [];
    
    // جمع كل الأسئلة من جميع التصنيفات
    for (let category in questionsBank) {
        allQuestions = [...allQuestions, ...questionsBank[category]];
    }
    
    // خلط الأسئلة عشوائياً
    for (let i = allQuestions.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [allQuestions[i], allQuestions[j]] = [allQuestions[j], allQuestions[i]];
    }
    
    // أخذ أول count سؤال
    return allQuestions.slice(0, count);
}

